    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        customer cs=new customer(jTextField1.getText(), jPasswordField1.getText(), jTextField3.getText(), jTextField2.getText(), Integer.parseInt(jTextField4.getText()));
        try {
            if(!database.getusername(jTextField1.getText())){
                database.registerusername(cs);
               clear();
                  JOptionPane.showMessageDialog(null,"registered successfully","Done",JOptionPane.PLAIN_MESSAGE);
                  Home h=new Home();
                   this.setVisible(false);
                  h.show();
                 
            }
            else{
                clear();
                  JOptionPane.showMessageDialog(null,"registered failed,Username Exists !","Error",JOptionPane.PLAIN_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(register.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

